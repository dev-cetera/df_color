// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

/// Performs a linear interpolation blend. Ideal for gradients and UI designs.
final class LerpColorBlender extends ColorBlender {
  //
  //
  //

  const LerpColorBlender({
    super.color1,
    super.color2,
    super.blendWeight = 0.5,
  });

  //
  //
  //

  @override
  Color $blend(Color c1, Color c2, double blendWeight) {
    return Color.lerp(c1, c2, blendWeight)!;
  }
}
