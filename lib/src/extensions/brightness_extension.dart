import 'dart:ui' show Brightness;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

extension BrightnessExtension on Brightness {
  Brightness get inverted {
    return this == Brightness.light ? Brightness.dark : Brightness.light;
  }
}
